    private String getNextDate() {
        int year;
        int month;
        int nextMonth;
        year=Integer.parseInt(splitDate[0]);
        month=Integer.parseInt(splitDate[1]);
        nextMonth=month+3;
        if(nextMonth>11){
            year++;
            nextMonth=nextMonth-12;
        }
        nextDonation=Integer.toString(year)+"-"+Integer.toString(nextMonth)+"-"+splitDate[2];
        return nextDonation;
    }
